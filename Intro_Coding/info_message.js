function printInfo(firstName, lastName, age, town) {
  return `You are ${firstName} ${lastName}, a ${age}-years old person from ${town}.`;
}

// Examples
console.log(printInfo("John", "Doe", 25, "New York"));
console.log(printInfo("Maria", "Smith", 30, "London"));
console.log(printInfo("Peter", "Johnson", 45, "Paris"));
